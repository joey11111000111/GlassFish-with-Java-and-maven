package proto;

public class MyInteger {

    private int intValue;

    public MyInteger() {
        intValue = 0;
    }

    public MyInteger(int intValue) {
        super();
        this.intValue = intValue;
    }

    public int getIntValue() {
        return intValue;
    }

    public void setIntValue(int intValue) {
        this.intValue = intValue;
    }

}